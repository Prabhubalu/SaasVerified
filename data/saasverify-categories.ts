/**
 * Search categories by keyword
 */
export function searchCategories(keyword: string): string[] {
  const lowerKeyword = keyword.toLowerCase();
  return saasverifyCategories.filter((cat) =>
    cat.toLowerCase().includes(lowerKeyword)
  );
}

/**
 * Get unique categories (removes duplicates)
 */
export function getUniqueCategories(): string[] {
  return Array.from(new Set(saasverifyCategories));
}

